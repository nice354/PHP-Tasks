<?php

/*
№11 — Найти количество секунд в сутках
Неверный вариант:
$a = 24 / 60 / 60;
echo $a;
Ошибка: деление вместо умножения — получается ~0.006, а не 86400
*/

$a = 24 * 60 * 60;
echo $a;
echo "<br>";

/*
№12 — Сложить две дроби (должно быть 2.5)
Неверный вариант:
echo 1. 2 + 1. 3;
Ошибка: пробелы внутри числа — синтаксическая ошибка
*/

echo 1.2 + 1.3;
echo "<br>";

/*
№13 — Получить последний символ строки
Неверный вариант:
$str = 'abc';
echo $str[strlen($str)];
Ошибка: строки индексируются с 0, последний индекс = strlen - 1
*/

$str = 'abc';
echo $str[strlen($str) - 1];
echo "<br>";

/*
№14 — Найти сумму всех цифр числа 123
Неверный вариант:
$num = (str) 123;
echo $num[1] + $num[2] + $num[3];
Ошибки: приведение (str) не существует, нужно (string);
индексы начинаются с 0, а не с 1
*/

$num = (string) 123;
echo $num[0] + $num[1] + $num[2];
echo "<br>";

/*
№15 — Найти сумму чисел
Неверный вариант:
$a = 123;
$b = 456;
$s = $a . $b;
echo $s;
Ошибка: оператор . — конкатенация строк, выведет "123456", а не 579
*/

$a = 123;
$b = 456;
$s = $a + $b;
echo $s;
echo "<br>";

/*
№16 — Найти сумму двух дробей (ожидаем 6)
Неверный вариант:
$num1 = (int) '2.5';
$num2 = (int) '3.5';
echo $num1 + $num2;
Ошибка: (int) обрезает дробную часть: 2+3=5, нужно приводить к float
*/

$num1 = (float) '2.5';
$num2 = (float) '3.5';
echo $num1 + $num2;
echo "<br>";
